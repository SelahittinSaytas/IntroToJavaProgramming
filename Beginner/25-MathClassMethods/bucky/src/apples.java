
// 25 - Math Class Methods

public class apples {
    public static void main(String[] args) {
        System.out.println(Math.abs(-26.7)); // Absolute value
        System.out.println(Math.ceil(7.4)); // Rounds up any number
        System.out.println(Math.floor(7.8)); // Rounds down any number
        System.out.println(Math.max(8.6, 5.2)); // max gives you the maximum number - takes multiple parameters in a single method
        System.out.println(Math.min(8.6, 5.2)); // min gives you the minimum number
        System.out.println(Math.pow(5, 3)); // 5 to the 3rd power
        System.out.println(Math.sqrt(9)); // Square Root
    }
}