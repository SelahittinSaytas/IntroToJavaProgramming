
abstract public class food {
    // 'abstract' means you cannot create an object from that class, but you can use
    // that class for things such as 'inheritance' and 'polymorphism', and stuff like that
    public abstract void eat();
    // An 'abstract' method in your superclass doesn't need a body
    // Anytime you have an 'abstract' method in the superclass, that entire class needs
    // to be labeled 'abstract' too
}
